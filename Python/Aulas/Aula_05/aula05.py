# Tipo INT e FLOAT + TYPE ( tipo de dado )

print(10, type(10)) # int
print(50, type(50)) # int

print(1.1, type(1.1)) # float
print(5.5, type(5.5)) # float

# teste
print(type(-1))
print(type(-5.5))

"""
OBS: se tiver casa decimal, será do tipo float, se não tiver casa decimal 
será do tipo int

atalho: CTRL + PONTO E VÍRGULA ( ; ) comenta as linhas selecionadas
"""